// 1. What is JavaScript?
// ================================
// JavaScript (JS) is a programming language that makes websites interactive.
// Think of a website as a toy car:
// - HTML: body of the car (structure)
// - CSS: paint and design (style)
// - JavaScript: engine that makes it move and work


// 2. History / Birth of JavaScript
// ================================
// Created in 1995 by Brendan Eich at Netscape
// Original names: Mocha -> LiveScript -> JavaScript
// Purpose: Make webpages interactive


// 3. Where JavaScript runs
// ================================
// - Web browsers (Chrome, Firefox, Safari)
// - Servers using Node.js


// Example
// Simple log
console.log("Hello! JavaScript");


// 4. How JavaScript is added to a webpage
// ================================
// 1. Inline JS
// <button onclick="console.log('Button clicked!')">Click Me</button>

// 2. Internal JS
// <script>
//    console.log("This is internal JavaScript!");
// </script>

// 3. External JS
// In file script.js:
// console.log("I am an external JS file!");
// Link in HTML:
// <script src="script.js"></script>



// 5. First Simple Example
// ================================

// Examples
// Display a message in console
console.log("Hello Rohan Singh");

// Add two numbers
let a = 5;
let b = 3;
console.log("5 + 3 =", a + b); // Output: 8


// 6. Why JavaScript is popular
// ================================
// - Runs in all browsers → Works everywhere
// - Makes websites interactive → Fun and dynamic
// - Huge community → Lots of help and tools
